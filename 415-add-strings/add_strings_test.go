package solution

import (
	"testing"
)

func TestAddStrings(t *testing.T) {
	cases := []struct {
		ina  string
		inb  string
		want string
	}{
		{"0", "0", "0"},
		{"1", "2", "3"},
		{"1", "9", "10"},
		{"22", "33", "55"}, {"111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111", "111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111111", "222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222222"},
	}

	for _, c := range cases {
		if got := addStrings(c.ina, c.inb); got != c.want {
			t.Errorf(
				"\n input: %s, %s \n got: %s \n want: %s",
				c.ina,
				c.inb,
				got,
				c.want,
			)
		}
	}
}
